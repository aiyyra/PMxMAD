package com.example.westudy;

public class Appointment {
    public String TVDateContent;
    public String TVSlotContent;
    public String TVCourseContent;
    public String TVTeacherContent;
    public String TVMessageContent;

    public Appointment(String TVDateContent, String TVSlotContent, String TVCourseContent, String TVTeacherContent, String TVMessageContent) {
        this.TVDateContent = TVDateContent;
        this.TVSlotContent = TVSlotContent;
        this.TVCourseContent = TVCourseContent;
        this.TVTeacherContent = TVTeacherContent;
        this.TVMessageContent = TVMessageContent;
    }
}
